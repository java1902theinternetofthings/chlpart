package com.hxzy.boot.znglpt.entity;

import javax.persistence.Entity;
import javax.persistence.Id;

@Entity
public class Station {
	@Id
	private int id;
	private String name;
	private String kind;
	private double x;
	private double y;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
}
